package JavaBasics;

public class Strings {
    public static void main(String[] args) {
        System.out.println("Madlibs!");

        // string is a text that goes to double quotes
        String username = "magicPony777";

        System.out.println("My ___ sister accidently ____ed all over her new ___");

        String adjective = "flamboyant";
        String verb = "fart";
        String noun = "pogo stick";

        System.out.println("My " + adjective + " sister accidently " + verb + "ed all over her new " + noun);

    }
}
